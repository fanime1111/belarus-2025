    function showNewsModal(news) {
      const modal = document.getElementById('news-modal');
      document.getElementById('news-modal-title').textContent = news.title;
      document.getElementById('news-modal-date').textContent = news.date || '';
      document.getElementById('news-modal-desc').innerHTML = news.desc.replace(/</g,'&lt;').replace(/\n/g,'<br>');
      const photosDiv = document.getElementById('news-modal-photos');
      photosDiv.innerHTML = '';
      if (news.photos && news.photos.length) {
        news.photos.forEach(src => {
          const img = document.createElement('img');
          img.src = src;
          img.alt = 'фото';
          photosDiv.appendChild(img);
        });
      }
      modal.style.display = 'flex';
      modal.setAttribute('aria-modal', 'true');
      modal.setAttribute('tabindex', '0');
      setTimeout(() => { modal.focus(); }, 10);
    }
    function hideNewsModal() {
      const modal = document.getElementById('news-modal');
      modal.style.display = 'none';
      modal.removeAttribute('aria-modal');
    }
    document.addEventListener('DOMContentLoaded', function() {
      const modal = document.getElementById('news-modal');
      if (modal) {
        document.getElementById('close-news-modal').onclick = hideNewsModal;
        const overlay = document.querySelector('.news-modal-overlay');
        if (overlay) overlay.onclick = hideNewsModal;
        modal.addEventListener('keydown', function(e) {
          if (e.key === 'Escape') hideNewsModal();
        });
      }
      document.querySelectorAll('.news-list-container').forEach(container => {
        container.addEventListener('click', function(e) {
          let block = e.target.closest('.news-block');
          if (block) {
            const blocks = Array.from(container.querySelectorAll('.news-block'));
            const idx = blocks.indexOf(block);
            if (idx !== -1 && typeof getNewsList === 'function') {
              const newsArr = getNewsList();
              if (newsArr[idx]) showNewsModal(newsArr[idx]);
            }
          }
        });
      });
    });
    function addDetailsButtonsToNews() {
      document.querySelectorAll('.news-list-container .news-block').forEach((block, idx) => {
        let btn = block.querySelector('.news-details-btn');
        if (!btn) {
          btn = document.createElement('button');
          btn.className = 'news-details-btn';
          btn.textContent = 'Подробнее';
          btn.style.position = 'absolute';
          btn.style.right = '18px';
          btn.style.bottom = '18px';
          btn.style.padding = '8px 18px';
          btn.style.background = 'var(--primary-green, #00A450)';
          btn.style.color = '#fff';
          btn.style.border = 'none';
          btn.style.borderRadius = '6px';
          btn.style.fontWeight = 'bold';
          btn.style.cursor = 'pointer';
          btn.style.boxShadow = '0 2px 8px rgba(0,0,0,0.10)';
          btn.style.zIndex = '3';
          btn.style.transition = 'background 0.2s';
          btn.onmouseenter = () => btn.style.background = '#008c5a';
          btn.onmouseleave = () => btn.style.background = 'var(--primary-green, #00A450)';
          btn.onclick = function(e) {
            e.stopPropagation();
            if (typeof getNewsList === 'function' && typeof showNewsModal === 'function') {
              const newsArr = getNewsList();
              if (newsArr[idx]) showNewsModal(newsArr[idx]);
            }
          };
          block.style.position = 'relative';
          block.appendChild(btn);
        }
      });
    }
    document.addEventListener('DOMContentLoaded', function() {
      addDetailsButtonsToNews();
    });
